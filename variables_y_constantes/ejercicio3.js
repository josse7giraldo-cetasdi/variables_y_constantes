/* Escribe un programa que le pida al usuario ingresar un número, luego le debe pedir un
segundo número e imprima en la consola la suma de ambos números que ingresó el
usuario */
let numero1 = parseFloat(prompt("ingrese un numero"));
let numero2 = parseFloat(prompt("ingrese  otro numero"));
let resultado = numero2 + numero1;
console.log(resultado);
document.write(resultado);
